MOV A, #38H
ACALL LCD_COMMAND
MOV A, #0EH
ACALL LCD_COMMAND
MOV A, #01H
ACALL LCD_COMMAND
MOV A, #82H
ACALL LCD_COMMAND
MOV DPTR, #STRING

BACK: CLR A
MOVC A, @A+DPTR
JZ EXIT
ACALL LCD_DATA
INC DPTR
SJMP BACK

EXIT:SJMP EXIT

LCD_COMMAND: ACALL DELAY
MOV P2, A
CLR P0.5	; RS=0
CLR P0.6	; R/!W = 0
SETB P0.7	; EN=1
ACALL DELAY
CLR P0.7	; EN=0
RET

LCD_DATA: ACALL DELAY
MOV P2, A
SETB P0.5	; RS=1
CLR P0.6	; R/!W=0
SETB P0.7	; EN=1
CLR P0.7	; EN=0
RET

DELAY: MOV R0, #0FFH
L1: MOV R1, #0FFH
L2: DJNZ R1, L2
DJNZ R0, L1
RET

STRING: DB 'PRATEEK',0
END